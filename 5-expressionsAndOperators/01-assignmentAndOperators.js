//assignment operator
let x= 20;

//addition assignment operator
x += 1; // x = x + 1
console.log(x);

//subtraction assignment operator
x -= 1; // x = x - 1
console.log(x);

//multiplication operator 
x *= 2; // x = x * 2
console.log(x);

//divison assignment operator
x /= 4; // x = x / 4
console.log(x);

//remainder (modulus) assignment assignment operator
x %= 2 // x = x % 2
console.log(x);

//modulus operator
console.log(5%3); // 5/3 -> 1 2/3 -> 2
console.log(25%7);  // 25/7 -> 3 4/7 -> 4

//exponentioal assignment operator
x **= 2;
console.log(x);

