//equality comparison operator
console.log('3' == 3); //true
console.log(4 == 4);//true
console.log(3 == '4');//false

//strict equality comparison operator
console.log(3 === 3);//true
console.log('3' === 3);//false

//not equal comparison operator
console.log('3'!= 4);//true
console.log('3' != 3);

//strict not equal comparison operator
console.log('3' !== 3);
console.log(3 !== 3);

//greater than
console.log(3>2);
console.log(2>3);

//less than
console.log(2<3);
console.log(3<2);

//greater than equal to
console.log(3>=2);
console.log(3>=3);

//less than equal to
console.log(2<=3);
console.log(3<=2);

//and (both expression on either side of the operator must be true for 'And' to return true)
console.log(1<2 && 3>0);
console.log(2<1 && 3>0);

//or (either expresion on one side of the operator must be true for 'or' to return true)
console.log(1<2 || 3<0);
console.log(2<1 || 3<0);

